package com.task4;
public class PrintAlphabets {
	public static void main(String[] args) {
        // Loop through the ASCII values of A to Z
        for (char ch = 'A'; ch <= 'Z'; ch++) {
            System.out.print(ch + " ");
        }
    }
}

